// integer 2-log (for numbers>1023)
int ilog2(uint32_t l) {
  int p = 0;
  if (l == 0) return 0;
  if (l & 0xFFF00000) {
    p = 20;
    l >>= 20;
  }
  if (l & 0xFFC00) {
    p += 10;
    l >>= 10;
  }
  p += log2_tab[l];
  return p;
}

int quantize_log(int val, int maxval, int sign, int levels) {
  if (levels==1 || val==0) return 0;
  int rlevels=(sign ? (levels-1)/2 : levels-1);
  int pre=(log4k[abs(4096/val)]*rlevels)/(log4k[4096/maxval-1]);
  int result = 1+pre;
  if (sign) {
    return 1+pre*2+(val<0);
  } else {
//    fprintf(stderr,"val:%i, maxval:%i, levels:%i, pre:%i\n",val,maxval,levels,pre);
    if (result >= levels) result=levels-1;
    assert(result < levels);
    assert(result > 0);
    return result;
  }
}

int quantize_log_uint32(uint32_t val, int levels) {
  if (val==0 || levels==1) return 0;
  levels--;
  int add=0;
  while (val < (1UL<<31)) {
    val *= 2;
    add++;
  }
  int app=(val >= 0xFFF80000) ? 4096 : (val+(1<<19))/(1<<20);
  int l=log4k[app]+add*log4k[2048]; // 0..174752 (5461 voor val=1<<31)
  int pre=((l-1)*levels)/174752;
  int result = levels-pre;
  if (result >= levels+1) result=levels;
  assert(result < levels+1);
  assert(result > 0);
  return result;
}
